# Taking input from user
num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))

print("Results:")

# Performing operations
print(num1, "+", num2, "=", num1 + num2)
print(num1, "-", num2, "=", num1 - num2)
print(num1, "*", num2, "=", num1 * num2)
print(num1, "/", num2, "=", round(num1 / num2, 2))
print(num1, "%", num2, "=", num1 % num2)
print(num1, "^", num2, "=", num1 ** num2)